int suma (int op1, int op2){
    return op1+op2;

}

int resto (int op1, int op2){
    return op1-op2;

}

int multiplica (int op1, int op2){
    return op1*op2;

}

int divide (int op1, int op2){
    return op1/op2;

}

int mayor (int op1, int op2){
    if(op1>op2){
        return op1;

    }else{
        return op2;
    }
}

